// The fundamental difference between arrays and linked lists
// is that arrays are static data structures, while linked lists
// are dynamic data structures. A static data structure needs
// all of its resources to be allocated when the structure is created;
// this means that even if the structure was to grow or shrink in size
// and elements were to be added or removed, it still always needs
// a given size and amount of memory. If more elements needed to be
// added to a static data structure and it didn’t have enough memory,
// you’d need to copy the data of that array, for example,
// and recreate it with more memory, so that you could add elements to it.